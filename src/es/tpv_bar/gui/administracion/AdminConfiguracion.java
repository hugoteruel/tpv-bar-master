    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        
        this.configuracionSelect.setClave(this.txtclave.getText());
        this.configuracionSelect.setValue(this.txtvalue.getText());
        this.configuracion.saveDato(this.configuracionSelect);
        this.jTableArrayList1.setList(configuracion.getLista());
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTableArrayList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableArrayList1MouseClicked
      
        this.configuracionSelect = (Configuracion) this.jTableArrayList1.getSelectElement();
        this.txtclave.setText(this.configuracionSelect.getClave());
        this.txtvalue.setText(this.configuracionSelect.getValue());
    }//GEN-LAST:event_jTableArrayList1MouseClicked

